CREATE OR REPLACE PROCEDURE NUMEROFORMATEADO (NUM_FORMATEAR IN OUT
VARCHAR2)
IS
    BLOQUE1 VARCHAR2(20);
    BLOQUE2 VARCHAR2(20);
    BLOQUE3 VARCHAR2(20);
    BLOQUE4 VARCHAR2(20);
BEGIN
    BLOQUE1:=substr(NUM_FORMATEAR,1,4);
    BLOQUE2:=substr(NUM_FORMATEAR,5,4);
    BLOQUE3:=substr(NUM_FORMATEAR,9,2);
    BLOQUE4:=substr(NUM_FORMATEAR,10);
NUM_FORMATEAR:=BLOQUE1 || '-' || BLOQUE2 || '-' || BLOQUE3 || '-' || BLOQUE4;
END;
/

SET SERVEROUTPUT ON
DECLARE
  NUM_FORMATEAR VARCHAR(30);
begin
  NUM_FORMATEAR:='11111111111111111111';
  DBMS_OUTPUT.PUT_LINE('FORMATO ORIGINAL: '||NUM_FORMATEAR);
  NUMEROFORMATEADO(NUM_FORMATEAR);
  DBMS_OUTPUT.PUT_LINE('NUEVO FORMATO: '||NUM_FORMATEAR);
end;