CREATE OR REPLACE PROCEDURE VISUALIZAR
(EMPL IN EMPLOYEES.DEPARTMENT_ID%TYPE, cantidad_empl OUT NUMBER)
IS
    CURSOR CURSOR_EMPL IS SELECT FIRST_NAME, SALARY, DEPARTMENT_ID FROM EMPLOYEES WHERE DEPARTMENT_ID=EMPL;
    NOMBRE_EMPL EMPLOYEES.FIRST_NAME%TYPE;
    SALARIO EMPLOYEES.SALARY%TYPE;
BEGIN
    CANTIDAD_EMPL:=0;
    FOR I IN CURSOR_EMPL LOOP
    DBMS_OUTPUT.PUT_LINE(I.DEPARTMENT_ID||' '||I.FIRST_NAME||' '||I.SALARY);
    CANTIDAD_EMPL:=CANTIDAD_EMPL+1;
    END LOOP;
END;
/

SET SERVEROUTPUT ON
DECLARE
  PARAMETRO_DEP NUMBER;
  CANTIDAD NUMBER;
begin
  PARAMETRO_DEP:=50;
  VISUALIZAR(PARAMETRO_DEP,CANTIDAD);
  DBMS_OUTPUT.PUT_LINE('CANTIDAD DE EMPLEADOS: '||CANTIDAD);
end;

