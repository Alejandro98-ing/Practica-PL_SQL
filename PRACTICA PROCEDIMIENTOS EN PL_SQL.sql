CREATE OR REPLACE PROCEDURE VISUALIZAR
IS
    CURSOR CURSOR_EMPL IS SELECT FIRST_NAME, SALARY FROM EMPLOYEES;
    NOMBRE_EMPL EMPLOYEES.FIRST_NAME%TYPE;
    SALARIO EMPLOYEES.SALARY%TYPE;
BEGIN
    FOR I IN CURSOR_EMPL LOOP
    DBMS_OUTPUT.PUT_LINE(I.FIRST_NAME||' '||I.SALARY);
    END LOOP;
END;
/

EXECUTE VISUALIZAR;
